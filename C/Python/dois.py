#Trabalhando com variáveis
#Operando números matematicamente
#Inserindo valores e definindo variáveis
x=int(input('Insira um valor inteiro de 0 a 9: '))
print('O valor inteiro escolhido foi:  ',x)
y=int(input('Isira um valor inteiro negativo de -1 a -9: '))
#Definindo variáveis das operaçães matemáticas
A=x+y   
B=x-y
C=x*y
D=x/y
#Retornando os resultados requeridos para o usuário
print('A soma de x e y é:',A)
print('A subtração entre x e y é:',B)
print('O produto entre x e y é:',C)
print('A razão entre x e y é:',D)